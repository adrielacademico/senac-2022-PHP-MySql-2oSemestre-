<?php
//valor inicial // condicional // incrementa + 1
for($cont = 0; $cont <= 5; $cont++){
    echo "--- Contador é $cont ---" . PHP_EOL;
    sleep(2);
}

//valor inicial // condicional // decrementa - 1
for($cont = 5; $cont >= 0; $cont--){
    echo "--- Contador é $cont ---" . PHP_EOL;
    sleep(2);
}